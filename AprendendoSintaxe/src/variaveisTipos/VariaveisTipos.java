package variaveisTipos;

public class VariaveisTipos {

	public static void main(String[] args) {
		//tipos de variaveis
		byte idade = 12; // byte menos dos tipos de variaveis
		short anoAtual = 2024; // short segundo menos comportando de -32.768 até 32.767
		int valor = 20000; // int um dos tipos mais usados 
		long numeroAleatorio = 3232345; //long sendo o maior tipo que comporta mais.
		
		// Tipos decimais:
        float PI = 3.14F; // Sempre acompanha um F no final do num para identificar na leitura de que é do tipo float e não do tipo double
        double salario = 2390.90;
        
        // variaveis e constante
        // ultiliza-se "final" para informar que aquela variavel não poderar ser alterada
        final int SALARIO = 1200;
	}

}
