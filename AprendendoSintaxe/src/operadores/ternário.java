package operadores;

public class ternário {

	public static void main(String[] args) {
		
		// representadas por "?" e ":"
		//<expressão condicional> ? <caso condição seja true> : <caso condição seja false>
		
		int a,b;
		
		a = 6;
		b = 6;
		
		String resultado = a==b ? "verdadeiro" : "falso";
		
		System.out.println(resultado);
		

	}

}
