package Estruturas;

public class DoWhile {

	public static void main(String[] args) {
		
		//do while 
		// o programa vai se rodado e depois testado
		
		int cont = 0;
		
		do {
			System.out.println("contando " + cont);
			cont++;
		}while(cont < 0);
	
		System.out.println("fim do programa");
	}
	
}
