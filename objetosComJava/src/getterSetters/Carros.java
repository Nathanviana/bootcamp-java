package getterSetters;

public class Carros {
	private String marca;
	private String cor;
	private int ano;
	
	public String getMarca() {
		return marca;
	}
	
	public void setMarca(String newMarca) {
		marca = newMarca;
	}
	
	public String getCor() {
		return cor;
	}
	
	public void setCor(String newCor) {
		cor = newCor;
	}
	
	public int getAno() {
		return ano;
	}
	
	public void setAno(int newAno) {
		this.ano = newAno;
	}
	
	
}
